/**
 * @param {string} s
 * @param {string} t
 * @return {boolean}
 */
var isAnagram = function (s, t) {
    if(s.length !== t.length) return false
    const map = {}
    for (let i of s) {
        map[i] = (map[i] || 0) + 1
    }

    for (let i of t) {
        if (!map[i]) return false
        map[i]--
    }
    return true
};